def hex_to_binary(hex_value):
    b = ''.join(f"{int(hex_value[i],16):04b}" for i in range(len(hex_value)))
    return b

def decode_six_bit(hex_value):
    payload_bin = hex_to_binary(hex_value)
    sb=[]
    started=False
    for i in range(0, len(payload_bin)-5, 6):
        chunk = payload_bin[i:i+6]
        if chunk == '000000':
            if not started:
                continue
            break
        started=True
        sb.append(ASCII6_MAP.get(chunk,'?'))
    return ''.join(sb)

